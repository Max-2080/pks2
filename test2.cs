using System;

class Program
{
    static void Main()
    {
        try
        {
            // –ü–æ–ª—É—á–∞–µ–º –Ω–æ–º–µ—Ä –±–∏–ª–µ—Ç–∞ –æ—Ç –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è
            Console.Write("–í–≤–µ–¥–∏—Ç–µ —à–µ—Å—Ç–∏–∑–Ω–∞—á–Ω—ã–π –Ω–æ–º–µ—Ä –±–∏–ª–µ—Ç–∞: ");
            string ticket = Console.ReadLine();

            // –ü—Ä–æ–≤–µ—Ä—è–µ–º, —á—Ç–æ —Å—Ç—Ä–æ–∫–∞ –Ω–µ –ø—É—Å—Ç–∞—è
            if (string.IsNullOrWhiteSpace(ticket))
            {
                Console.WriteLine("–û—à–∏–±–∫–∞: –Ω–æ–º–µ—Ä –±–∏–ª–µ—Ç–∞ –Ω–µ –º–æ–∂–µ—Ç –±—ã—Ç—å –ø—É—Å—Ç—ã–º");
                return;
            }

            // –£–¥–∞–ª—è–µ–º –≤–æ–∑–º–æ–∂–Ω—ã–µ –ø—Ä–æ–±–µ–ª—ã
            ticket = ticket.Trim();

            // –ü—Ä–æ–≤–µ—Ä—è–µ–º –∫–æ—Ä—Ä–µ–∫—Ç–Ω–æ—Å—Ç—å –≤–≤–æ–¥–∞: –¥–ª–∏–Ω–∞ –∏ —á—Ç–æ –≤—Å–µ —Å–∏–º–≤–æ–ª—ã - —Ü–∏—Ñ—Ä—ã
            if (ticket.Length != 6)
            {
                Console.WriteLine("–û—à–∏–±–∫–∞: –Ω–æ–º–µ—Ä –±–∏–ª–µ—Ç–∞ –¥–æ–ª–∂–µ–Ω —Å–æ—Å—Ç–æ—è—Ç—å –∏–∑ 6 —Ü–∏—Ñ—Ä");
                return;
            }

            if (!IsNumber(ticket))
            {
                Console.WriteLine("–û—à–∏–±–∫–∞: –Ω–æ–º–µ—Ä –±–∏–ª–µ—Ç–∞ –¥–æ–ª–∂–µ–Ω —Å–æ–¥–µ—Ä–∂–∞—Ç—å —Ç–æ–ª—å–∫–æ —Ü–∏—Ñ—Ä—ã");
                return;
            }

            // –ü—Ä–µ–æ–±—Ä–∞–∑—É–µ–º —Å—Ç—Ä–æ–∫—É –≤ –º–∞—Å—Å–∏–≤ —Ü–∏—Ñ—Ä
            int[] digits = new int[6];
            for (int i = 0; i < 6; i++)
            {
                // –ö–∞–∂–¥—ã–π —Å–∏–º–≤–æ–ª —Å—Ç—Ä–æ–∫–∏ –ø—Ä–µ–æ–±—Ä–∞–∑—É–µ–º –≤ —Ü–∏—Ñ—Ä—É
                digits[i] = int.Parse(ticket[i].ToString());
            }

            // –í—ã—á–∏—Å–ª—è–µ–º —Å—É–º–º—É –ø–µ—Ä–≤—ã—Ö —Ç—Ä–µ—Ö —Ü–∏—Ñ—Ä
            int sumFirst = digits[0] + digits[1] + digits[2];
            
            // –í—ã—á–∏—Å–ª—è–µ–º —Å—É–º–º—É –ø–æ—Å–ª–µ–¥–Ω–∏—Ö —Ç—Ä–µ—Ö —Ü–∏—Ñ—Ä
            int sumLast = digits[3] + digits[4] + digits[5];

            // –í—ã–≤–æ–¥–∏–º –ø—Ä–æ–º–µ–∂—É—Ç–æ—á–Ω—ã–µ —Å—É–º–º—ã –¥–ª—è –Ω–∞–≥–ª—è–¥–Ω–æ—Å—Ç–∏
            Console.WriteLine($"–°—É–º–º–∞ –ø–µ—Ä–≤—ã—Ö —Ç—Ä–µ—Ö —Ü–∏—Ñ—Ä: {sumFirst}");
            Console.WriteLine($"–°—É–º–º–∞ –ø–æ—Å–ª–µ–¥–Ω–∏—Ö —Ç—Ä–µ—Ö —Ü–∏—Ñ—Ä: {sumLast}");

            // –ü—Ä–æ–≤–µ—Ä—è–µ–º, —è–≤–ª—è–µ—Ç—Å—è –ª–∏ –±–∏–ª–µ—Ç —Å—á–∞—Å—Ç–ª–∏–≤—ã–º
            if (sumFirst == sumLast)
            {
                Console.WriteLine("–ë–∏–ª–µ—Ç —Å—á–∞—Å—Ç–ª–∏–≤—ã–π! üéâ");
            }
            else
            {
                Console.WriteLine("–ë–∏–ª–µ—Ç –æ–±—ã—á–Ω—ã–π.");
            }
        }
        catch (FormatException)
        {
            // –û–±—Ä–∞–±–∞—Ç—ã–≤–∞–µ–º –æ—à–∏–±–∫–∏ –ø—Ä–µ–æ–±—Ä–∞–∑–æ–≤–∞–Ω–∏—è —Ç–∏–ø–æ–≤
            Console.WriteLine("–û—à–∏–±–∫–∞: –Ω–µ–≤–µ—Ä–Ω—ã–π —Ñ–æ—Ä–º–∞—Ç –¥–∞–Ω–Ω—ã—Ö –ø—Ä–∏ –ø—Ä–µ–æ–±—Ä–∞–∑–æ–≤–∞–Ω–∏–∏");
        }
        catch (OverflowException)
        {
            // –û–±—Ä–∞–±–∞—Ç—ã–≤–∞–µ–º –ø–µ—Ä–µ–ø–æ–ª–Ω–µ–Ω–∏–µ (—Ö–æ—Ç—è –º–∞–ª–æ–≤–µ—Ä–æ—è—Ç–Ω–æ –¥–ª—è 6 —Ü–∏—Ñ—Ä)
            Console.WriteLine("–û—à–∏–±–∫–∞: —á–∏—Å–ª–æ —Å–ª–∏—à–∫–æ–º –±–æ–ª—å—à–æ–µ");
        }
        catch (Exception ex)
        {
            // –û–±—Ä–∞–±–∞—Ç—ã–≤–∞–µ–º –≤—Å–µ –æ—Å—Ç–∞–ª—å–Ω—ã–µ –Ω–µ–ø—Ä–µ–¥–≤–∏–¥–µ–Ω–Ω—ã–µ –æ—à–∏–±–∫–∏
            Console.WriteLine($"–ü—Ä–æ–∏–∑–æ—à–ª–∞ –Ω–µ–ø—Ä–µ–¥–≤–∏–¥–µ–Ω–Ω–∞—è –æ—à–∏–±–∫–∞: {ex.Message}");
        }
        finally
        {
            // –≠—Ç–æ—Ç –±–ª–æ–∫ –≤—ã–ø–æ–ª–Ω—è–µ—Ç—Å—è –≤—Å–µ–≥–¥–∞, –¥–∞–∂–µ –µ—Å–ª–∏ –±—ã–ª–∞ –æ—à–∏–±–∫–∞
            Console.WriteLine("\n–ü—Ä–æ–≥—Ä–∞–º–º–∞ –∑–∞–≤–µ—Ä—à–µ–Ω–∞. –°–ø–∞—Å–∏–±–æ –∑–∞ –∏—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∏–µ!");
        }
    }

    // –í—Å–ø–æ–º–æ–≥–∞—Ç–µ–ª—å–Ω—ã–π –º–µ—Ç–æ–¥ –¥–ª—è –ø—Ä–æ–≤–µ—Ä–∫–∏, —è–≤–ª—è–µ—Ç—Å—è –ª–∏ —Å—Ç—Ä–æ–∫–∞ —á–∏—Å–ª–æ–º
    // –ü—Ä–æ–≤–µ—Ä—è–µ—Ç, —á—Ç–æ –∫–∞–∂–¥—ã–π —Å–∏–º–≤–æ–ª –≤ —Å—Ç—Ä–æ–∫–µ - —Ü–∏—Ñ—Ä–∞ –æ—Ç 0 –¥–æ 9
    static bool IsNumber(string str)
    {
        // –ü—Ä–æ–≤–µ—Ä—è–µ–º –∫–∞–∂–¥—ã–π —Å–∏–º–≤–æ–ª –≤ —Å—Ç—Ä–æ–∫–µ
        foreach (char c in str)
        {
            // –ï—Å–ª–∏ —Å–∏–º–≤–æ–ª –Ω–µ –Ω–∞—Ö–æ–¥–∏—Ç—Å—è –≤ –¥–∏–∞–ø–∞–∑–æ–Ω–µ —Ü–∏—Ñ—Ä '0'-'9'
            if (c < '0' || c > '9')
                return false; // –ù–∞–π–¥–µ–Ω –Ω–µ—Ü–∏—Ñ—Ä–æ–≤–æ–π —Å–∏–º–≤–æ–ª
        }
        return true; // –í—Å–µ —Å–∏–º–≤–æ–ª—ã - —Ü–∏—Ñ—Ä—ã
    }
}